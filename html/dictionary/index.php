<h2 class="simple">Р</h2>

<p><strong><a name="rastvor"></a>Растворитель</strong> преобразует компоненты, находящиеся в твердом состоянии в жидкое или пастообразную форму, обеспечивая, таким образом, возможность нанесения их на поверхность кожи. Кроме того, растворители способствуют очищению кожи от остатков старого крема. Кремы с органическими растворителями, как правило, обладают большей влагостойкостью, т.к. могут содержать большее количество восков в составе по сравнению с кремом на водной основе.</p>


