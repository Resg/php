<h2 class="simple">П</h2>

<p><strong><a name="parafin"></a>Парафин</strong> получается из парафинового масла. Защищает кожу от воздействия внешней влаги, поскольку не растворим в воде. </p>

<p><strong><a name="PAV"></a>ПАВ</strong> (поверхностно-активные вещества, тензиды) – химически активные соединения. Тензиды улучшают смачивамость поверхности, так как снижают поверхностное натяжение. Они легко проникают между частицами материалов, на которые их наносят. Тензиды также являются важнейшим ингредиентом всех моющих средств. Классический пример тензидов - мыло. </p>

<p><strong><a name="penopul"></a>Пенополиуретан</strong> – отличный теплогидроизолятор, обладающий очень низкой теплопроводностью. Кроме того, этот материал обладает амортизирующим эффектом за счет пористой структуры. Пенополиуретан устойчив к действию бактерий и грибков. </p>

<p><strong><a name="poliur_gel"></a>Полиуретановый гель (термопластичный эластомерный гель)</strong> – специальный материал, идеально подходящий для производства гелевых вставок для обуви. Супермягкий и долговечный, он позволяет эффективно защищать проблемные участки от натирания и сдавливания, стабильно держит форму и легко моется. Материал абсолютно безопасен для кожи. </p>

