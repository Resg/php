<h2 class="simple">М</h2>

<p><strong><a name="kamfor_oil"></a>Масло Камфорное</strong> (белое) получают из древесины и корней камфорного дерева, растущего в Японии и Китае. Обладает хорошими смягчающими свойствами. </p>

<p><strong><a name="norka_oil"></a>Масло Норковое</strong> получается из норкового жира. Масло защищает кожу от потери влаги. Обладает очень хорошими регенерирующими свойствами.  Норковое масло имеет в своем составе очень необычный тип полиненасыщенных жирных кислот и глицеридов, а также комплекс витаминов, эффективно ухаживающих за кожей.</p>

<p><strong><a name="silicon"></a>Масло Силиконовое</strong> придает составу водостойкость, обладает хорошей распределяемостью по поверхности. Не закупоривает поры кожи, что позволяет коже «дышать».
В случае с замшевыми кожами используется особый род силиконов – диметиконы. Эти вещества полностью впитываются в кожу и не оставляют пятен на замшевой коже. </p>

<p><strong><a name="mindal"></a>Миндальное масло</strong> &nbsp;&mdash; растительное масло, получаемое из&nbsp;семян миндаля, оказывает смягчающее, питательное и&nbsp;защитное действие на&nbsp;кожу.</p>


<p><strong><a name="mikrofibra"></a>Микрофибра</strong> - это текстиль нового поколения. Само название "микрофибра" пришло из технологии производства ультратонких волокон (эти волокна в сто раз тоньше человеческого волоса). Микрофибра обладает высокой прочностью; впитывает гораздо больше влаги, чем обычная ткань, благодаря очень малому диаметру сечения нити; не подвержена воздействию бытовой грязи, что обеспечивается структурой волокна; не линяет, не скатывается, не оставляет после себя волокон, быстро высыхает. Все эти свойства делают ее идеальным материалом для производства обувных стелек. </p>


