<h2 class="simple">Ш</h2>

<p><strong>Шерсть натуральная</strong> – это волокна, которые в основном получают из шерсти овец (реже используется шерсть и пух других животных). Шерсть упруга, эластична, прочна. Имеет высокую гигроскопичность (способность поглощать влагу) – может впитать в себя до 40% влаги относительно собственного веса, при этом не кажется мокрой на ощупь. Натуральная шерсть имеет свойство оставаться чистой, так как при нормальных условиях шерсть слабо электризуется и не удерживает частицы пыли. Натуральная шерсть позволяет телу дышать и содержит его в сухом тепле.</p>



